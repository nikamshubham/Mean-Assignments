let addition = (n1, n2) => {
    return n1 + n2
}

let substraction = function (n1, n2) {
    return n1 - n2
}

let multiplication = function (n1, n2) {
    return n1 * n2
}

let division = function (n1, n2) {
    return n1 / n2
}

module.exports = {
    addition,
    substraction,
    multiplication,
    division
}